 private String botonSeleccionado(){
        String btn_seleccionado = "";
        if (radbtn_persona.isSelected()){
             btn_seleccionado = "Persona";
        }else if (radbtn_empresa.isSelected()){
            btn_seleccionado = "Empresa";
        }
        return btn_seleccionado;
    }
